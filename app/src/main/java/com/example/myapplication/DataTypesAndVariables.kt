package com.example.myapplication

fun main() {
    /*
    Variables:-
    ------------------------------------------------
    Used for storing the values in memory.
    var -> Mutable = Can change the value.
    val -> Immutable = Can't change the value.

    */

    var varVariableName = "value"
    val valVariableName = "value"

    /*
    Data Types:-
    ------------------------------------------------

    Integer Types:- [9]
    */
    val myByte: Byte = 100
    val myShort: Short = 5000
    val myInt: Int = 100000
    val myLong: Long = 15000000000L
    /*

    Floating Point Types:-
    */
    val myFloat: Float = 5.75F
    val myDouble: Double = 19.99
    /*

    Scientific Numbers:-
    */
    val myFloatSN: Float = 35E3F
    val myDoubleSN: Double = 12E4
    /*

    Booleans:-
    */
    val isKotlinFun: Boolean = true
    val isFishTasty: Boolean = false
    /*

    Characters:-
    */
    val myGrade: Char = 'B'
    /*

    BiometricManager.Strings:-
    */
    val myText: String = "Hello World"

}